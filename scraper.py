def scrape_municode(url, btype):
    res = {'url':url,'date':'','items':[],'raw':0,'status':''}
    resp = fetch(url)
    if not resp: res['status']='could not reach site'; return res
    soup = BeautifulSoup(resp.text,'lxml')
    links = []
    for a in soup.find_all('a', href=True):
        h = a['href']; t = a.get_text(strip=True)
        if not h.startswith('http'): h = requests.compat.urljoin(url,h)
        fl = (t+' '+h).lower()
        if 'meetings.municode' in fl or 'municode.com/meet' in fl or 'agenda' in fl:
            d = fdate(t+' '+h)
            links.append({'url':h,'date':d,'text':t,'pdf':h.lower().endswith('.pdf')})
    for a in soup.find_all('a', href=True):
        h = a['href']; t = a.get_text(strip=True)
        if not h.startswith('http'): h = requests.compat.urljoin(url,h)
        if h.lower().endswith('.pdf'):
            d = fdate(t+' '+h)
            if d: links.append({'url':h,'date':d,'text':t,'pdf':True})
    links.sort(key=lambda x:x['date'] if x['date'] else '', reverse=True)
    recent = [l for l in links if l['date']>=CUTOFF]
    for lnk in recent[:5]:
        if lnk['pdf']:
            lines = pdf_text(lnk['url'])
            if lines:
                items = filt(lines)
                if items:
                    res['items']=items; res['date']=lnk['date']; res['url']=lnk['url']; res['raw']=len(lines)
                    return res
        else:
            sub = fetch(lnk['url'])
            if sub:
                ss = BeautifulSoup(sub.text,'lxml')
                for a2 in ss.find_all('a', href=True):
                    h2 = a2['href']
                    if not h2.startswith('http'): h2 = requests.compat.urljoin(lnk['url'],h2)
                    if h2.lower().endswith('.pdf') or 'ViewFile' in h2:
                        lines = pdf_text(h2)
                        if lines:
                            items = filt(lines)
                            if items:
                                res['items']=items; res['date']=lnk['date']; res['url']=h2; res['raw']=len(lines)
                                return res
                        time.sleep(0.3)
                hi = []
                for el in ss.select('li, p, td, .agenda-item, [class*="agenda"], [class*="item"]'):
                    t = el.get_text(separator=' ', strip=True)
                    if 20 < len(t) < 800: hi.append(t)
                if hi:
                    items = filt(hi)
                    if items:
                        res['items']=items; res['date']=lnk['date']; res['url']=lnk['url']; res['raw']=len(hi)
                        return res
            time.sleep(0.5)
    if not res['status']: res['status']='no recent agenda items found'
    return res

