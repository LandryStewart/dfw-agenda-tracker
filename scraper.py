def scrape_cp(url, btype):
    res = {'url':'','date':'','items':[],'raw':0,'status':''}
    resp = fetch(url)
    if not resp: res['status']='could not reach site'; return res
    soup = BeautifulSoup(resp.text,'lxml')
    allp = find_pdfs(soup, url)
    for a in soup.find_all('a', href=True):
        h = a['href']
        if not h.startswith('http'): h = requests.compat.urljoin(url,h)
        if '/AgendaCenter/' in h and h != url and any(t in a.get_text(strip=True).lower() for t in (PZ if btype=='pz' else CC)):
            cr = fetch(h)
            if cr:
                allp.extend(find_pdfs(BeautifulSoup(cr.text,'lxml'), h))
                time.sleep(0.3)
    ags = [p for p in allp if p['type']=='ag' and (p['board']==btype or p['board']=='unk') and p['date']>=CUTOFF]
    if not ags: ags = [p for p in allp if p['type']=='ag' and p['date']>=CUTOFF]
    if not ags: res['status']='no recent agenda PDFs found'; return res
    ags.sort(key=lambda x:x['date'], reverse=True)
    for ag in ags[:3]:
        lines = pdf_text(ag['url'])
        if lines:
            items = filt(lines)
            if items:
                res['items']=items; res['date']=ag['date']; res['url']=ag['url']; res['raw']=len(lines)
                mns = [p for p in allp if p['type']=='min' and p['date']==ag['date']]
                if not mns: mns = [p for p in allp if p['type']=='min']
                for mn in mns[:1]:
                    ml = pdf_text(mn['url'])
                    if ml:
                        votes = get_votes(ml, items)
                        if votes: res['items'] = [i+' ['+votes[i]+']' if i in votes else i for i in items]
                    time.sleep(0.3)
                return res
            else:
                res['status'] = f'PDF parsed ({len(lines)} lines) but no rezone/plat/comp plan items'
                res['date']=ag['date']; res['url']=ag['url']; res['raw']=len(lines)
        time.sleep(0.5)
    if not res['status']: res['status']='PDFs found but could not extract text'
    return res

