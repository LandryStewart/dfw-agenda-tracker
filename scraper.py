def scrape_board(url, city, bname):
    bt = 'pz' if 'Zoning' in bname else 'cc'
    r = {'board':bname,'agenda_url':url,'meeting_date':'','items':[],'sfr_items':[],'status':'success'}
    try:
        if '/AgendaCenter' in url:
            d = scrape_cp(url, bt)
        elif 'legistar.com' in url:
            d = scrape_legistar(url, bt)
        elif 'municode' in url.lower() or any(x in url.lower() for x in ['pondertx.com','cityofheartland']):
            d = scrape_municode(url, bt)
        else:
            d = scrape_gen(url, bt)
        r['items']=d['items']; r['meeting_date']=d.get('date',''); r['agenda_url']=d.get('url',url)
        if d.get('status') and not d['items']: r['status']=d['status']
        for it in r['items']:
            kw = [k for k in SFR_KW if k.lower() in it.lower()]
            if kw: r['sfr_items'].append(it+' [Keywords: '+', '.join(kw[:5])+']')
    except Exception as e:
        r['status']='error: '+str(e)[:120]
    return r

