def scrape_city(m):
    data = {'name':m['n'],'county':m['co'],'boards':[]}
    same_url = m['pz'] == m['cc']
    seen_items = set()
    for key, bname in [('pz','Planning & Zoning'),('cc','City Council')]:
        url = m[key]
        print(f"    {bname}...", end=' ', flush=True)
        r = scrape_board(url, m['n'], bname)
        if same_url and key == 'cc':
            r['items'] = [i for i in r['items'] if i[:80].lower() not in seen_items]
            r['sfr_items'] = [i for i in r['sfr_items'] if i.split(' [Keywords')[0][:80].lower() not in seen_items]
            if not r['items']: r['status'] = 'same agenda center - items shown under P&Z'
        if key == 'pz':
            seen_items = {i[:80].lower() for i in r['items']}
        print(f"{len(r['items'])} items | {r['status']}")
        data['boards'].append(r)
