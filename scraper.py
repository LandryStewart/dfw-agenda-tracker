    re.compile(r'(approved|denied|tabled|continued|withdrawn|passed|failed)\s*(?:by a vote of\s*)?(\d+\s*[-to]+\s*\d+)?', re.I),
    re.compile(r'(unanimous(?:ly)?)\s*(approved|passed|denied)?', re.I),
