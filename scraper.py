def pdate(raw):
    for f in ['%B %d %Y','%b %d %Y','%m/%d/%Y','%m-%d-%Y','%Y-%m-%d','%Y/%m/%d']:
        try: return datetime.strptime(raw.strip().replace(',',''),f).strftime('%Y-%m-%d')
        except: pass
    return ''

def fdate(text):
    for p in DATE_P:
        m = p.search(text)
        if m:
            d = pdate(m.group(1))
            if d and d >= CUTOFF: return d
    return ''

def fetch(url, t=25):
    try:
        r = requests.get(url, headers=HEADERS, timeout=t, allow_redirects=True)
        r.raise_for_status(); return r
    except: return None

def pdf_text(url):
    try:
        import pdfplumber, io
        r = requests.get(url, headers=HEADERS, timeout=45); r.raise_for_status()
        lines = []
        with pdfplumber.open(io.BytesIO(r.content)) as pdf:
            for pg in pdf.pages[:20]:
                t = pg.extract_text()
                if t:
                    for ln in t.split('\n'):
                        ln = ln.strip()
                        if len(ln) > 15: lines.append(ln)
        return lines
    except: return []

def is_pdf_junk(t):
    tl = t.lower()
    if len(t) < 25: return True
    if re.match(r'^[\d\s:/.,-]+$', tl): return True
    for p in PDF_JUNK_C:
        if p.search(tl): return True
    digits = sum(1 for c in t if c.isdigit())
    if len(t) > 0 and digits/len(t) > 0.5: return True
    return False

def is_noise(t):
    tl = t.lower()
    for p in NOISE:
        if p in tl: return True
    if re.match(r'^(www\.|http|tel:|fax:|email:|phone:)', tl): return True
    return False

def is_board_only(t):
    tl = t.lower()
    bw = ['committee','board','commission','subcommittee','task force']
    aw = ['amend','change','update','request','case','hearing','rezone','plat',
          'approve','consider','recommend','deny','acres','lots','district','from','to']
    return any(b in tl for b in bw) and not any(a in tl for a in aw)

def is_action(t):
    if is_board_only(t): return False
    tl = t.lower()
    return any(s.lower() in tl for s in SIGNALS)

def merge_lines(lines):
    merged = []; buf = ''
    for ln in lines:
        if is_pdf_junk(ln): continue
        if ln and ln[0].islower() and buf:
            buf += ' ' + ln
        elif re.match(r'^[\u2022\u2023\-\*]', ln) or re.match(r'^\d+\.', ln):
            if buf: merged.append(buf.strip())
            buf = re.sub(r'^[\u2022\u2023\-\*]\s*', '', ln)
            buf = re.sub(r'^\d+\.\s*', '', buf)
        else:
            if buf: merged.append(buf.strip())
            buf = ln
    if buf: merged.append(buf.strip())
    return merged

def filt(lines):
    merged = merge_lines(lines)
    out = []; seen = set()
    for ln in merged:
        ln = re.sub(r'\s+', ' ', ln).strip()
        if is_noise(ln) or is_pdf_junk(ln): continue
        if not is_action(ln): continue
        k = ln[:80].lower()
        if k in seen: continue
        seen.add(k); out.append(ln)
    return out

def get_votes(ml, items):
    d = {}
    for it in items:
        ws = [w for w in it.lower().split() if len(w)>4][:5]
        near = ' '.join(m for m in ml if any(w in m.lower() for w in ws[:3]))
        for vp in VOTE_P:
            vm = vp.search(near)
            if vm:
                pts = [g for g in vm.groups() if g]
                if pts: d[it] = ' '.join(pts).upper().strip(); break
    return d

def find_pdfs(soup, base):
    pdfs = []
    for a in soup.find_all('a', href=True):
        href = a['href']
        if not href.startswith('http'): href = requests.compat.urljoin(base, href)
        txt = a.get_text(strip=True)
        ctx = ''
        for p in a.parents:
            if p.name in ['tr','div','li','section','td','h2','h3','h4','span']:
                ctx = p.get_text(separator=' ', strip=True)[:400]; break
        is_ag = 'ViewFile/Agenda' in href or (href.lower().endswith('.pdf') and 'agenda' in (txt+ctx).lower())
        is_mn = 'ViewFile/Minutes' in href or (href.lower().endswith('.pdf') and 'minute' in (txt+ctx).lower())
        if not is_ag and not is_mn: continue
        dt = fdate(txt+' '+href+' '+ctx)
        full = (txt+' '+ctx).lower()
        bd = 'pz' if any(t in full for t in PZ) else ('cc' if any(t in full for t in CC) else 'unk')
        pdfs.append({'url':href,'date':dt,'board':bd,'type':'min' if is_mn else 'ag','ctx':full[:200]})
    return pdfs

